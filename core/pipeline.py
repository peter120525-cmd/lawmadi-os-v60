async def run_pipeline_stage1(query: str) -> RAGContext:
    """스트리밍용: Stage 1만 실행"""
    try:
        return await _stage1_rag_search(query)
    except Exception as e:
        logger.warning(f"[Stream Stage 1] RAG 실패: {e}")
        return RAGContext()


async def run_pipeline_stage2(
    query: str,
    analysis: Dict,
    rag_context: RAGContext,
    drf_verification: Optional[VerificationResult] = None,
    lang: str = "",
    mode: str = "general",
) -> str:
    """스트리밍용: Stage 2만 실행 (서킷브레이커 + 강화 프롬프트)"""
    if os.getenv("ENABLE_LAWMADILM", "true").lower() != "true":
        logger.info("[Stream Stage 2] LawmadiLM 스킵 (ENABLE_LAWMADILM=false)")
        return ""
    cb = _cb_state()
    if cb == "open":
        logger.info("[Stream Stage 2] LawmadiLM 스킵 — 서킷 OPEN")
        return ""
    if cb == "half_open":
        logger.info("[Stream Stage 2] LawmadiLM 시험 호출 (서킷 HALF_OPEN)")
    try:
        raw = await _call_lawmadilm(
            query, analysis, rag_context,
            drf_verification=drf_verification,
            lang=lang,
            mode=mode,
        )
        draft = _postprocess_lawmadilm(raw, query) or ""
        if draft:
            _cb_record_success()
        return draft
    except httpx.HTTPStatusError as e:
        status = e.response.status_code if e.response else 0
        _cb_record_failure()
        if status == 503 and cb != "half_open":
            logger.warning(f"[Stream Stage 2] LawmadiLM 503 → 0.5초 후 재시도")
            await asyncio.sleep(0.5)
            try:
                raw = await _call_lawmadilm(
                    query, analysis, rag_context,
                    drf_verification=drf_verification,
                    lang=lang, mode=mode,
                )
                draft = _postprocess_lawmadilm(raw, query) or ""
                if draft:
                    _cb_record_success()
                return draft
            except Exception:
                _cb_record_failure()
        logger.warning(f"[Stream Stage 2] LawmadiLM {status} 실패: {e}")
        return ""
    except Exception as e:
        _cb_record_failure()
        logger.warning(f"[Stream Stage 2] LawmadiLM 실패: {e}")
        return ""


async def run_pipeline_stage3(text: str, lang: str = "") -> VerificationResult:
    """스트리밍용: Stage 3만 실행 (20초 타임아웃)"""
    if not text:
        return VerificationResult()
    try:
        return await asyncio.wait_for(_drf_verify_law_refs(text, lang=lang), timeout=20.0)
    except asyncio.TimeoutError:
        logger.warning("[Stream Stage 3] DRF 검증 타임아웃 (20초) → 검증 스킵")
        return VerificationResult(drf_failed=True)
    except Exception as e:
        logger.error(f"[Stream Stage 3] DRF 검증 실패: {e} → 검증 스킵")
        return VerificationResult(drf_failed=True)
    return VerificationResult(drf_failed=True)


