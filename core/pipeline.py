def _extract_articles_from_drf(raw_response) -> List[Dict]:
    """DRF lawService.do 응답에서 조문 목록을 추출

    lawService.do 응답 구조: {"법령": {"조문": {"조문단위": [...]}}}
    각 조문단위: {"조문번호": "3", "조문내용": "제3조(대항력) ...", ...}
    """
    if not raw_response:
        return []

    articles = []
    try:
        if isinstance(raw_response, dict):
            # lawService.do 응답: {"법령": {"조문": {"조문단위": [...]}}}
            law_root = raw_response.get("법령", raw_response)
            if isinstance(law_root, dict):
                jo_section = law_root.get("조문", {})
                if isinstance(jo_section, dict):
                    jo_list = jo_section.get("조문단위", [])
                    if isinstance(jo_list, dict):
                        jo_list = [jo_list]
                    if isinstance(jo_list, list):
                        articles.extend(jo_list)

            # Fallback: LawSearch 응답 구조 (하위 호환)
            if not articles:
                law_data = raw_response.get("LawSearch", {})
                if isinstance(law_data, dict):
                    law_list = law_data.get("law", [])
                    if isinstance(law_list, dict):
                        law_list = [law_list]
                    if isinstance(law_list, list):
                        for law in law_list:
                            jo_list = law.get("조문", [])
                            if isinstance(jo_list, dict):
                                jo_list = [jo_list]
                            for jo in jo_list:
                                if isinstance(jo, dict):
                                    articles.append(jo)

            # 직접 리스트 형식
            if not articles:
                for key in ["law", "조문", "조문목록"]:
                    items = raw_response.get(key, [])
                    if isinstance(items, list):
                        articles.extend(items)
                    elif isinstance(items, dict):
                        articles.append(items)

        elif isinstance(raw_response, list):
            articles = raw_response

    except Exception as e:
        logger.debug(f"[DRF Parse] 조문 추출 실패: {e}")

    return articles


def _get_article_text(articles: List[Dict], article_num: int, suffix: str = "") -> Optional[str]:
    """조문 목록에서 특정 조문번호(+suffix)의 텍스트를 반환"""
    art_label = f"제{article_num}조{suffix}"
    for art in articles:
        try:
            if _match_article_num(art, article_num, suffix):
                content = art.get("조문내용", "") or art.get("조문", "") or art.get("content", "")
                title = art.get("조문제목", "") or art.get("제목", "")
                if content:
                    return f"{art_label}({title}) {content}" if title else f"{art_label} {content}"
        except (ValueError, TypeError):
            continue
    return None


def _extract_articles_from_elaw(raw_response) -> List[Dict]:
    """elaw lawService.do 응답에서 영문 조문 목록 추출

    elaw 응답 구조: {"Law": {"JoSection": {"Jo": [{"joNo": "0015", "joCts": "...", "joYn": "Y"}, ...]}}}
    joNo: 4자리 조문번호 (예: "0015" → 15조)
    joCts: 조문 영문 내용
    joYn: "Y"=실제 조문, "N"=삭제 조문

    Returns:
        [{"article_num": 15, "content": "..."}, ...] (joYn=="Y"인 것만)
    """
    if not raw_response:
        return []

    articles = []
    try:
        if isinstance(raw_response, dict):
            law_root = raw_response.get("Law", raw_response)
            if isinstance(law_root, dict):
                jo_section = law_root.get("JoSection", {})
                if isinstance(jo_section, dict):
                    jo_list = jo_section.get("Jo", [])
                    if isinstance(jo_list, dict):
                        jo_list = [jo_list]
                    if isinstance(jo_list, list):
                        for jo in jo_list:
                            if not isinstance(jo, dict):
                                continue
                            if jo.get("joYn", "Y") != "Y":
                                continue
                            jo_no = jo.get("joNo", "")
                            try:
                                art_num = int(jo_no.lstrip("0") or "0")
                            except (ValueError, TypeError):
                                continue
                            if art_num <= 0:
                                continue
                            content = jo.get("joCts", "") or ""
                            articles.append({
                                "article_num": art_num,
                                "content": content,
                            })
    except Exception as e:
        logger.debug(f"[elaw Parse] 조문 추출 실패: {e}")

    return articles


# 영문 법률 참조 regex (모듈 레벨 컴파일)
_EN_LAW_REF_RE = re.compile(
    r'([\w][\w\s]*(?:ACT|CODE|DECREE|LAW|RULE|REGULATION))\s*'
    r'(?:Article|Art\.?)\s*(\d+)',
    re.IGNORECASE,
)


async def _drf_verify_law_refs(text: str, lang: str = "") -> VerificationResult:
    """Stage 4: 응답에서 인용된 모든 법률+판례 참조를 DRF API로 전수 검증.
    법률 fetch와 판례 fetch를 동시 실행하여 0.5~1.5초 절약.
    lang="en" 시 영문 법률 참조도 추출하여 검증.
    """
    result = VerificationResult()

    # ── 1) 법률 참조 + 판례 참조 동시 추출 (regex, instant) ──
    # ⚠️ (?:[가-힣]+\s*)+ 는 catastrophic backtracking 유발
    #    → [가-힣]+(?:\s+[가-힣]+)* 로 교체 (whitespace+로 분리, 중첩 반복 제거)
    refs = re.findall(
        r'([가-힣]+(?:\s+[가-힣]+)*\s*(?:등에\s+관한\s+)?(?:법률|법|시행령|시행규칙|규정|조례))\s*제(\d+)조(의\d+)?\s*(?:제(\d+)항)?',
        text
    )

    prec_refs = re.findall(
        r'((?:대법원|대법|헌법재판소|헌재|서울고등법원|서울고법|서울중앙지방법원)\s*'
        r'(\d{4})\s*[.]\s*(\d{1,2})\s*[.]\s*\d{1,2}\s*[.]?\s*선고\s*'
        r'(\d{2,4}[가-힣]+\d+)\s*(?:판결|결정))',
        text
    )
    if not prec_refs:
        prec_refs_simple = re.findall(
            r'(\d{2,4}'
            r'(?:헌바|헌마|헌가|헌나|헌라|헌사|헌아|헌자|'
            r'다|나|가|마|카|타|파|라|바|사|아|자|차|하|'
            r'두|누|구|무|부|수|우|주|추|후|그|드|스|으)'
            r'(?:합)?\d{2,6})',
            text
        )
    else:
        prec_refs_simple = [p[3] for p in prec_refs]

    # ── 1-b) 영문 법률 참조 추출 (lang="en" 시) ──
    en_refs = []
    if lang == "en":
        en_refs = _EN_LAW_REF_RE.findall(text)

    if not refs and not prec_refs_simple and not en_refs:
        result.all_passed = True
        return result

    svc = _RUNTIME.get("search_service")
    if not svc and not refs and not en_refs:
        # 법률 없고 서비스도 없으면 판례만 검증 가능 여부 확인
        pass
    elif not svc and not en_refs:
        logger.warning("[Stage 4] SearchService 없음 -> 검증 스킵")
        result.all_passed = True
        return result

    # ── 2) 법률·판례 고유 목록 추출 ──
    seen = set()
    unique_laws = set()
    ref_items = []
    for law_name, article_num_str, suffix_str, paragraph_str in refs:
        law_name = law_name.strip()
        # 법령명 앞에 붙은 비법률 텍스트 제거 (예: "지금 바로 하실 수 있는 일 민법" → "민법")
        # [가-힣]+(?:법률|법|...) 은 최소 1자 + 접미사라 "법률"/"법" 단독은 매칭 불가 → 안전
        if ' ' in law_name:
            _m_law = re.search(r'([가-힣]+(?:법률|법|시행령|시행규칙|규정|조례))$', law_name)
            if _m_law and _m_law.group(1) != law_name:
                law_name = _m_law.group(1)
        article_num = int(article_num_str)
        suffix = suffix_str.strip() if suffix_str else ""
        key = f"{law_name} 제{article_num}조{suffix}"
        if key in seen:
            continue
        seen.add(key)
        ref_items.append((law_name, article_num, suffix, key))
        unique_laws.add(law_name)

    # ── 2-b) 영문 법률 고유 목록 추출 ──
    en_ref_items = []         # (en_name, article_num, key, kr_name_or_None)
    en_kr_laws = set()        # 한글 변환 성공 → 기존 한글 DRF 플로우 사용
    en_elaw_laws = set()      # 변환 실패 → elaw API 직접 검증
    for en_name_raw, art_num_str in en_refs:
        en_name = en_name_raw.strip()
        article_num = int(art_num_str)
        key = f"{en_name} Article {article_num}"
        if key in seen:
            continue
        seen.add(key)
        # _EN_TO_KR_LAW_MAP으로 한글 법명 변환 시도
        kr_name = _EN_TO_KR_LAW_MAP.get(en_name.upper())
        en_ref_items.append((en_name, article_num, key, kr_name))
        if kr_name:
            en_kr_laws.add(kr_name)
            unique_laws.add(kr_name)  # 한글 DRF fetch 대상에 추가
        else:
            en_elaw_laws.add(en_name)

    prec_seen = set()
    unique_cases = []
    for case_no in prec_refs_simple:
        case_no = case_no.strip()
        if case_no in prec_seen or len(case_no) < 5:
            continue
        prec_seen.add(case_no)
        unique_cases.append(case_no)

    # ── 3) 법률 fetch + 판례 fetch 동시 실행 (asyncio.gather) ──
    # ⚠️ async DRF 코드에 이벤트 루프 블로킹 요소가 있어
    #    sync 버전 전체를 asyncio.to_thread로 실행 (스레드 내부에서 requests.get timeout 정상 작동)
    _FETCH_ERROR = "FETCH_ERROR"

    async def _fetch_law(ln: str):
        try:
            raw = await asyncio.to_thread(svc.get_law_articles, ln)
            return ln, raw
        except Exception as e:
            logger.warning(f"[Stage 4] {ln} 법령 조회 실패: {e}")
            return ln, _FETCH_ERROR

    drf_inst = _RUNTIME.get("drf")

    async def _fetch_prec(cn: str):
        try:
            raw_prec = None
            if drf_inst and hasattr(drf_inst, "search_precedents"):
                raw_prec = await asyncio.to_thread(drf_inst.search_precedents, cn)
            return cn, raw_prec, None
        except Exception as e:
            return cn, None, e

    # 영문 elaw 법령 fetch (한글 변환 실패한 것만)
    async def _fetch_elaw(en_name: str):
        try:
            if drf_inst and hasattr(drf_inst, "get_elaw_articles"):
                raw = await asyncio.to_thread(drf_inst.get_elaw_articles, en_name)
                return en_name, raw
            return en_name, None
        except Exception as e:
            logger.warning(f"[Stage 4] {en_name} elaw 조회 실패: {e}")
            return en_name, _FETCH_ERROR

    # 법률 + 판례 + elaw fetch를 하나의 gather로 동시 실행
    law_tasks = [_fetch_law(ln) for ln in unique_laws] if unique_laws and svc else []
    prec_tasks = [_fetch_prec(cn) for cn in unique_cases] if unique_cases and drf_inst else []
    elaw_tasks = [_fetch_elaw(en) for en in en_elaw_laws] if en_elaw_laws and drf_inst else []

    all_fetch_results = []
    if law_tasks or prec_tasks or elaw_tasks:
        try:
            all_fetch_results = await asyncio.wait_for(
                asyncio.gather(*law_tasks, *prec_tasks, *elaw_tasks, return_exceptions=True),
                timeout=15.0,
            )
        except asyncio.TimeoutError:
            logger.warning(f"[Stage 4] DRF fetch 타임아웃 (15초) — 법률 {len(law_tasks)}건, 판례 {len(prec_tasks)}건, elaw {len(elaw_tasks)}건")
            result.all_passed = False
            result.drf_failed = True
            return result

    # 결과 분리 (return_exceptions=True: exception은 결과로 반환됨)
    law_articles_cache: Dict[str, Any] = {}
    law_result_count = len(law_tasks)
    prec_result_count = len(prec_tasks)
    elaw_result_count = len(elaw_tasks)

    for i, res in enumerate(all_fetch_results[:law_result_count]):
        if isinstance(res, BaseException):
            logger.warning(f"[Stage 4] 법률 fetch 예외: {res}")
            continue
        try:
            ln, raw = res
            law_articles_cache[ln] = raw
        except (ValueError, TypeError) as e:
            logger.warning(f"[Stage 4] 법률 결과 파싱 오류: {e}")

    prec_results_raw = []
    prec_start = law_result_count
    prec_end = prec_start + prec_result_count
    for res in all_fetch_results[prec_start:prec_end]:
        if isinstance(res, BaseException):
            logger.warning(f"[Stage 4] 판례 fetch 예외: {res}")
            continue
        prec_results_raw.append(res)
    if unique_cases and not drf_inst:
        logger.debug("[Stage 4] 판례 DRF 미사용 (drf 인스턴스 없음)")
        prec_results_raw = [(cn, None, None) for cn in unique_cases]

    # elaw 결과 분리
    elaw_articles_cache: Dict[str, Any] = {}
    elaw_start = prec_end
    for res in all_fetch_results[elaw_start:]:
        if isinstance(res, BaseException):
            logger.warning(f"[Stage 4] elaw fetch 예외: {res}")
            continue
        try:
            en_name, raw = res
            elaw_articles_cache[en_name] = raw
        except (ValueError, TypeError) as e:
            logger.warning(f"[Stage 4] elaw 결과 파싱 오류: {e}")

    # ── 4) 법률 조문별 검증 ──
    for law_name, article_num, suffix, key in ref_items:
        try:
            raw = law_articles_cache.get(law_name)

            # fetch 실패(Connection reset 등)와 실제 미존재를 구분
            if raw == _FETCH_ERROR:
                logger.warning(f"[Stage 4] ⚠️ {key}: DRF 조회 실패 → 검증 스킵")
                continue  # 미검증 목록에 추가하지 않음 (검증불가)

            law_exists = bool(raw)
            article_exists = False
            article_text = None
            articles = []

            if raw:
                articles = _extract_articles_from_drf(raw)
                if articles:
                    article_exists = any(
                        _match_article_num(a, article_num, suffix) for a in articles
                    )
                    article_text = _get_article_text(articles, article_num, suffix)

            ref_entry = {
                "ref": key,
                "law_name": law_name,
                "article_num": article_num,
                "law_exists": law_exists,
                "article_exists": article_exists,
                "article_text": article_text,
                "verified": law_exists and article_exists,
            }

            if ref_entry["verified"]:
                result.verified_refs.append(ref_entry)
                logger.info(f"[Stage 4] ✅ {key}: 검증 통과")
            else:
                reason = "법령 미존재" if not law_exists else f"제{article_num}조{suffix} 미존재"
                ref_entry["reason"] = reason
                result.unverified_refs.append(ref_entry)
                logger.warning(f"[Stage 4] ❌ {key}: {reason} (law_exists={law_exists}, articles={len(articles) if raw and articles else 0})")

        except Exception as e:
            logger.warning(f"[Stage 4] {key} 검증 실패: {e}")
            result.unverified_refs.append({
                "ref": key,
                "law_name": law_name,
                "article_num": article_num,
                "law_exists": False,
                "article_exists": False,
                "article_text": None,
                "verified": False,
                "reason": f"검증 오류: {type(e).__name__}",
            })

    # ── 4-b) 영문 법률 조문별 검증 ──
    for en_name, article_num, key, kr_name in en_ref_items:
        try:
            law_exists = False
            article_exists = False
            article_text = None

            if kr_name:
                # 한글 변환 성공 → 기존 한글 DRF 데이터로 검증
                raw = law_articles_cache.get(kr_name)
                if raw == _FETCH_ERROR:
                    logger.warning(f"[Stage 4] ⚠️ {key}: DRF 조회 실패 → 검증 스킵")
                    continue
                law_exists = bool(raw)
                if raw:
                    articles = _extract_articles_from_drf(raw)
                    if articles:
                        article_exists = any(
                            _match_article_num(a, article_num, "") for a in articles
                        )
                        article_text = _get_article_text(articles, article_num, "")
            else:
                # 한글 변환 실패 → elaw API 응답으로 직접 검증
                raw = elaw_articles_cache.get(en_name)
                if raw == _FETCH_ERROR:
                    logger.warning(f"[Stage 4] ⚠️ {key}: elaw 조회 실패 → 검증 스킵")
                    continue
                law_exists = bool(raw)
                if raw:
                    elaw_arts = _extract_articles_from_elaw(raw)
                    if elaw_arts:
                        for ea in elaw_arts:
                            if ea.get("article_num") == article_num:
                                article_exists = True
                                article_text = f"Article {article_num}: {ea.get('content', '')[:200]}"
                                break

            ref_entry = {
                "ref": key,
                "law_name": en_name,
                "article_num": article_num,
                "law_exists": law_exists,
                "article_exists": article_exists,
                "article_text": article_text,
                "verified": law_exists and article_exists,
                "type": "en_law",
            }

            if ref_entry["verified"]:
                result.verified_refs.append(ref_entry)
                logger.info(f"[Stage 4] ✅ {key}: 영문 검증 통과 (via {'KR DRF' if kr_name else 'elaw'})")
            else:
                reason = "법령 미존재" if not law_exists else f"Article {article_num} 미존재"
                ref_entry["reason"] = reason
                result.unverified_refs.append(ref_entry)
                logger.warning(f"[Stage 4] ❌ {key}: {reason}")

        except Exception as e:
            logger.warning(f"[Stage 4] {key} 영문 검증 실패: {e}")
            result.unverified_refs.append({
                "ref": key,
                "law_name": en_name,
                "article_num": article_num,
                "law_exists": False,
                "article_exists": False,
                "article_text": None,
                "verified": False,
                "type": "en_law",
                "reason": f"검증 오류: {type(e).__name__}",
            })

    # ── 5) 판례 검증 ──
    if prec_results_raw:
        for case_no, raw_prec, fetch_err in prec_results_raw:
            try:
                if fetch_err:
                    raise fetch_err

                prec_exists = bool(raw_prec)

                # ── 판례 내용 일치 검증: 판시사항/판결요지 추출 후 텍스트 비교 ──
                content_match = False
                drf_summary = ""
                if prec_exists and raw_prec:
                    drf_summary = _extract_precedent_summary(raw_prec)
                    if drf_summary:
                        cited_context = _extract_cited_context(text, case_no)
                        content_match = _check_precedent_content_match(drf_summary, cited_context)

                verified = prec_exists and (content_match or not drf_summary)

                ref_entry = {
                    "ref": f"판례 {case_no}",
                    "type": "precedent",
                    "case_no": case_no,
                    "verified": verified,
                    "prec_exists": prec_exists,
                    "content_match": content_match,
                    "drf_summary": drf_summary[:800] if drf_summary else "",
                }
                if verified:
                    result.verified_refs.append(ref_entry)
                    logger.info(f"[Stage 4] ✅ 판례 {case_no}: 검증 통과 (존재={prec_exists}, 내용일치={content_match})")
                else:
                    reason = "판례 미존재" if not prec_exists else "판시사항 내용 불일치"
                    ref_entry["reason"] = reason
                    result.unverified_refs.append(ref_entry)
                    logger.warning(f"[Stage 4] ❌ 판례 {case_no}: {reason}")
            except Exception as e:
                logger.warning(f"[Stage 4] 판례 {case_no} 검증 실패: {e}")
                result.unverified_refs.append({
                    "ref": f"판례 {case_no}",
                    "type": "precedent",
                    "case_no": case_no,
                    "verified": False,
                    "reason": f"검증 오류: {type(e).__name__}",
                })

    result.total_refs = len(result.verified_refs) + len(result.unverified_refs)
    result.all_passed = len(result.unverified_refs) == 0

    law_count = sum(1 for r in result.verified_refs if r.get("type") != "precedent")
    prec_count = sum(1 for r in result.verified_refs if r.get("type") == "precedent")
    law_fail = sum(1 for r in result.unverified_refs if r.get("type") != "precedent")
    prec_fail = sum(1 for r in result.unverified_refs if r.get("type") == "precedent")

    logger.info(
        f"[Stage 4] DRF 전수 검증 완료: "
        f"총 {result.total_refs}건 (법률 {law_count+law_fail}건, 판례 {prec_count+prec_fail}건) | "
        f"통과 {len(result.verified_refs)}건, "
        f"실패 {len(result.unverified_refs)}건"
    )

    return result


def _extract_precedent_summary(raw_prec: Any) -> str:
    """DRF 판례 응답에서 판시사항/판결요지/결정요지 텍스트를 추출"""
    summary_parts = []
    try:
        items = []
        if isinstance(raw_prec, dict):
            # PrecSearch 응답: {"PrecSearch": {"prec": [...]}} 또는 직접 리스트
            prec_root = raw_prec.get("PrecSearch", raw_prec)
            if isinstance(prec_root, dict):
                prec_list = prec_root.get("prec", [])
                if isinstance(prec_list, dict):
                    prec_list = [prec_list]
                items = prec_list if isinstance(prec_list, list) else []
            # 직접 판례 데이터
            for key in ["판시사항", "판결요지", "결정요지", "이유"]:
                val = raw_prec.get(key, "")
                if val and isinstance(val, str):
                    summary_parts.append(val[:500])
        elif isinstance(raw_prec, list):
            items = raw_prec

        for item in items[:1]:
            if isinstance(item, dict):
                for key in ["판시사항", "판결요지", "결정요지", "이유"]:
                    val = item.get(key, "")
                    if val and isinstance(val, str):
                        summary_parts.append(val[:500])
    except Exception as e:
        logger.debug(f"[Prec Summary] 추출 실패: {e}")

    return " ".join(summary_parts)[:1500]


def _extract_cited_context(text: str, case_no: str, window: int = 3) -> str:
    """응답 텍스트에서 판례번호 주변 문맥을 추출 (전후 window줄)"""
    lines = text.split("\n")
    context_lines = []
    for i, line in enumerate(lines):
        if case_no in line:
            start = max(0, i - window)
            end = min(len(lines), i + window + 1)
            context_lines.extend(lines[start:end])
    return " ".join(context_lines)[:2000]


def _check_precedent_content_match(drf_summary: str, cited_context: str) -> bool:
    """DRF 판시사항과 응답 내 인용 문맥의 내용 일치 여부를 확인.
    핵심 법률 키워드 3개 이상 일치하면 통과."""
    if not drf_summary or not cited_context:
        return False

    # DRF 판시사항에서 핵심 키워드 추출 (2글자 이상 한글 명사)
    drf_keywords = set(re.findall(r'[가-힣]{2,6}', drf_summary))
    # 너무 일반적인 단어 제외
    stopwords = {"하였다", "있다", "없다", "한다", "이다", "경우", "것으로", "대하여",
                 "아니라", "하여", "않는", "되는", "위한", "따라", "대한", "관한",
                 "같은", "있는", "없는", "하는", "된다", "된다", "이에", "그리고"}
    drf_keywords -= stopwords

    if len(drf_keywords) < 3:
        # 키워드가 너무 적으면 번호 존재만으로 통과
        return True

    # 인용 문맥에서 DRF 키워드 매칭
    match_count = sum(1 for kw in drf_keywords if kw in cited_context)
    match_ratio = match_count / len(drf_keywords) if drf_keywords else 0

    # 키워드 3개 이상 또는 20% 이상 일치하면 통과
    return match_count >= 3 or match_ratio >= 0.2


def _match_article_num(article_dict: Dict, target_num: int, suffix: str = "") -> bool:
    """조문 딕셔너리에서 조문번호가 target_num(+suffix)과 일치하는지 확인.
    suffix 예: "의2", "의3" (제839조의2 → target_num=839, suffix="의2")
    DRF 조문번호 형태: "10", "10의4", "839의2", "제10조의4", "0003의0002" 등
    """
    try:
        raw = article_dict.get("조문번호", "")
        # 정규화: "제", "조" 제거 → "10의4", "839의2", "10" 등
        num_str = str(raw).strip().replace("제", "").replace("조", "").strip()
        target_str = f"{target_num}{suffix}" if suffix else str(target_num)

        if num_str == target_str:
            return True

        # 앞자리 0 제거: "0003의0002" → "3의2", "0010" → "10"
        num_clean = re.sub(r'\b0+(\d)', r'\1', num_str)
        if num_clean == target_str:
            return True

        # suffix 없이 순수 숫자 비교 (기존 호환)
        if not suffix:
            try:
                return int(re.sub(r'\D.*', '', num_str)) == target_num
            except ValueError:
                pass

        # 조문내용에서 "제N조의M" 패턴 확인 (fallback)
        content = str(article_dict.get("조문내용", ""))
        if f"제{target_num}조{suffix}" in content[:50]:
            return True

    except (ValueError, TypeError):
        pass
    return False


