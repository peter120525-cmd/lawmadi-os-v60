    _LAW_ARTICLE_RE = re.compile(
        r'([가-힣][가-힣\s·\-]*(?:법|시행령|규칙|협약|특별법|특례법|기본법|촉진법))\s*제(\d+)조'
    )
    for _lid, _boost_kr in _LEADER_LAW_BOOST.items():
        _en_lines: List[str] = []
        for _line in _boost_kr.split("\n"):
            _line = _line.strip()
            if not _line.startswith("•"):
                continue
            _m = _LAW_ARTICLE_RE.search(_line)
            if _m:
                _law_name = _m.group(1).strip()
                _art_num = _m.group(2)
                _cached = _ELAW_CACHE.get(_law_name, {})
                _art_text = _cached.get("articles", {}).get(_art_num, "")
                if _art_text and _cached.get("name_en"):
                    _en_lines.append(f"• {_cached['name_en']} {_art_text}")
                    continue
            # 캐시에 없으면 한국어 원문 유지
            _en_lines.append(_line)
        if _en_lines:
            _LEADER_LAW_BOOST_EN[_lid] = "\n".join(_en_lines)
    logger.info(f"[elaw] 영문 LAW_BOOST 생성: {len(_LEADER_LAW_BOOST_EN)}개 리더")

