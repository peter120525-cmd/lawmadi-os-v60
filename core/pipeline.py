def _remove_unverified_refs(text: str, drf_verification: VerificationResult) -> str:
    """미검증 조문 참조를 텍스트에서 제거하거나 경고 태깅"""
    for ref_info in drf_verification.unverified_refs:
        ref = ref_info.get("ref", "")
        if ref in text:
            reason = ref_info.get("reason", "미확인")
            text = text.replace(ref, f"{ref}(※ {reason})")
    return text


def _strip_unverified_sentences(text: str, drf_verification: VerificationResult) -> str:
    """미검증 조문이 포함된 문장을 텍스트에서 완전 삭제"""
    lines = text.split("\n")
    bad_refs = [r.get("ref", "") for r in drf_verification.unverified_refs if r.get("ref")]
    cleaned = []
    for line in lines:
        if any(ref in line for ref in bad_refs):
            logger.debug(f"[STRIP] 미검증 조문 포함 문장 삭제: {line[:60]}...")
            continue
        cleaned.append(line)
    return "\n".join(cleaned)


def _apply_fail_closed(final_text: str, drf_verification: VerificationResult) -> str:
    """FAIL_CLOSED 로직: 0.1% 초과 미검증 시 응답 차단, DRF 자체 오류 시에도 차단"""

    # ── 안전장치 1: DRF 검증 시스템 자체 오류 → FAIL_CLOSED ──
    if drf_verification.drf_failed:
        logger.warning("[FAIL_CLOSED] DRF 검증 시스템 오류 → 응답 차단")
        return FAIL_CLOSED_RESPONSE

    # 법률 조문 참조가 없는 응답 → 통과 (일반 상담/설명)
    if drf_verification.total_refs == 0:
        return final_text

    # 법조문과 판례를 분리하여 FAIL_CLOSED 판단
    law_unverified = [r for r in drf_verification.unverified_refs if r.get("type") != "precedent"]
    prec_unverified = [r for r in drf_verification.unverified_refs if r.get("type") == "precedent"]
    law_total = sum(1 for r in drf_verification.verified_refs if r.get("type") != "precedent") + len(law_unverified)
    unverified_count = len(drf_verification.unverified_refs)

    # ── 안전장치 2: 법조문 미검증 비율 0.1% 초과 → 응답 차단 ──
    law_ratio = len(law_unverified) / max(law_total, 1) if law_total > 0 else 0
    if law_ratio > 0.001:
        logger.warning(
            f"[FAIL_CLOSED] 법조문 미검증 {len(law_unverified)}/{law_total} "
            f"({law_ratio*100:.1f}%) > 0.1% → 응답 차단"
        )
        return FAIL_CLOSED_RESPONSE

    # ── 안전장치 2-b: 미검증 판례 → 해당 문장만 삭제 (FAIL_CLOSED 아님) ──
    if prec_unverified:
        final_text = _strip_unverified_sentences(final_text, VerificationResult(
            unverified_refs=prec_unverified,
        ))
        logger.info(
            f"[SAFE] 미검증 판례 {len(prec_unverified)}건 문장 삭제"
        )

    # ── 안전장치 3: 미검증 법조문 → 해당 조문 완전 삭제 (태깅 아님) ──
    if law_unverified:
        final_text = _strip_unverified_sentences(final_text, VerificationResult(
            unverified_refs=law_unverified,
        ))
        logger.info(
            f"[SAFE] 미검증 법조문 {len(law_unverified)}건 문장 삭제 "
            f"({law_ratio*100:.1f}%)"
        )

    return final_text


