# Internal: NLU detect intent
# ====================================================================

def _nlu_detect_intent(query: str) -> Optional[str]:
    """NLU pattern-based legal intent detection -> leader ID (L01~L60).

    Uses (situation + action) regex combos for accurate colloquial Korean
    classification. When multiple domains match, decides by:
      (1) number of matching patterns (desc)
      (2) priority value (asc -- lower wins)
    """
    matches: List[tuple] = []  # (leader_id, match_count)

    for leader_id, compiled_patterns in _NLU_COMPILED.items():
        match_count = sum(1 for p in compiled_patterns if p.search(query))
        if match_count > 0:
            matches.append((leader_id, match_count))

    if not matches:
        return None

    # Sort: (1) match count desc  (2) priority asc (lower = higher priority)
    matches.sort(key=lambda x: (-x[1], _NLU_PRIORITY.get(x[0], 3)))
    best_id, _ = matches[0]
    return best_id


