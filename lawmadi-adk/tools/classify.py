# ====================================================================
# Internal: keyword matching via domain_map
# ====================================================================

def _keyword_detect(query: str) -> Optional[str]:
    """Score-based keyword matching across all 60 domains.
    Returns the leader_id with the highest keyword-hit count, or None.
    """
    max_score = 0
    selected_leader: Optional[str] = None

    for _, (keywords, leader_id) in _DOMAIN_MAP.items():
        score = sum(1 for k in keywords if k in query)
        if score > max_score:
            max_score = score
            selected_leader = leader_id

    return selected_leader if max_score > 0 else None


# ====================================================================
# Internal: name matching
# ====================================================================

def _name_detect(query: str) -> Optional[str]:
    """Detect explicit leader name calls in the query.
    Longer names are preferred; ties broken by earlier position.
    Returns leader_id or None.
    """
    matches: List[tuple] = []  # (leader_id, name, position)
    for name, lid in _NAME_TO_ID.items():
        pos = query.find(name)
        if pos >= 0:
            matches.append((lid, name, pos))

    if not matches:
        return None

    # Sort: (1) name length desc  (2) position asc
    matches.sort(key=lambda x: (-len(x[1]), x[2]))
    return matches[0][0]


# ====================================================================
# PUBLIC API: classify_query  (ADK FunctionTool target)
# ====================================================================

def classify_query(query: str) -> dict:
    """Classify a Korean legal query into the appropriate expert leader.

    Routing pipeline:
      1. Name matching  (explicit leader name in query)
      2. NLU pattern matching  (situation+action regex)
      3. Keyword matching  (domain keyword score)
      4. Fallback -> L60 (CCO)

    Args:
        query: The user's legal question in Korean.

    Returns:
        dict with keys:
            leader_id   (str):  e.g. "L08"
            leader_name (str):  e.g. "온유"
            method      (str):  "name" | "nlu" | "keyword" | "fallback"
            confidence  (float): 0.0 ~ 1.0
    """
    if not query or not query.strip():
        return {
            "leader_id": "L60",
            "leader_name": LEADER_NAMES["L60"],
            "method": "fallback",
            "confidence": 0.0,
        }

    query = query.strip()

    # --- 1) Name matching (highest confidence) ---
    name_id = _name_detect(query)
    if name_id:
        return {
            "leader_id": name_id,
            "leader_name": LEADER_NAMES.get(name_id, ""),
            "method": "name",
            "confidence": 1.0,
        }

    # --- 2) NLU pattern matching ---
    nlu_id = _nlu_detect_intent(query)
    if nlu_id:
        return {
            "leader_id": nlu_id,
            "leader_name": LEADER_NAMES.get(nlu_id, ""),
            "method": "nlu",
            "confidence": 0.9,
        }

    # --- 3) Keyword matching ---
    kw_id = _keyword_detect(query)
    if kw_id:
        return {
            "leader_id": kw_id,
            "leader_name": LEADER_NAMES.get(kw_id, ""),
            "method": "keyword",
            "confidence": 0.7,
        }

    # --- 4) Fallback -> L60 (CCO) ---
    return {
        "leader_id": "L60",
        "leader_name": LEADER_NAMES["L60"],
        "method": "fallback",
        "confidence": 0.3,
    }
